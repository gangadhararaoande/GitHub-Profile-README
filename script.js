function convertToTimestamp(dateString) {
  const [day, month, year, hours, minutes] = dateString.split(/[/ :apm]+/);
  const date = new Date(year, month - 1, day, hours, minutes);
  return date.getTime();
}

function createCategoryButton(categoryName) {
  const button = document.createElement("button");
  button.textContent = categoryName;
  return button;
}

function createCard(data) {
  const card = document.createElement("div");
  card.classList.add("card");

  const img = document.createElement("img");
  img.src = data.filePath;
  img.alt = data.nickName;
  card.appendChild(img);

  const info = document.createElement("div");
  info.classList.add("info");
  const timestamp = new Date(data.timestamp);
  const formattedTimestamp = timestamp.toLocaleString();
  info.innerHTML = `
    <a target="_blank" href="${
      new URL(data.githubUrl, "https://github.com").href
    }">${data.nickName}</a>
    <p>Capture Time: ${formattedTimestamp}</p>
  `;
  card.appendChild(info);

  card.addEventListener("click", () => {
    window.open(new URL(data.githubUrl, "https://github.com").href, "_blank");
  });

  return card;
}

function displayData(data) {
  data.forEach((categoryData) => {
    const categoryButton = createCategoryButton(categoryData.categoryName);
    categoriesContainer.appendChild(categoryButton);

    categoryData.list.forEach((data) => {
      const card = createCard(data);
      cardsContainer.appendChild(card);
    });
  });

  allButton.addEventListener("click", () => {
    categoriesContainer.innerHTML = "";
    cardsContainer.innerHTML = "";
    displayData(data);
  });

  categoriesContainer.addEventListener("click", (event) => {
    if (event.target.tagName === "BUTTON") {
      const categoryData = data.find(
        (category) => category.categoryName === event.target.textContent
      );
      cardsContainer.innerHTML = "";
      categoryData.list.forEach((data) => {
        const card = createCard(data);
        cardsContainer.appendChild(card);
      });
    }
  });

  closeModal.addEventListener("click", () => {
    modal.style.display = "none";
  });

  window.addEventListener("click", (event) => {
    if (event.target === modal) {
      modal.style.display = "none";
    }
  });
}

function filterCards(searchText) {
  const filteredData = data.flatMap((category) =>
    category.list.filter((card) =>
      card.nickName.toLowerCase().includes(searchText.toLowerCase())
    )
  );

  cardsContainer.innerHTML = "";

  filteredData.forEach((card) => {
    const cardElement = createCard(card);
    cardsContainer.appendChild(cardElement);
  });
}

searchInput.addEventListener("input", (event) => {
  filterCards(event.target.value);
});

